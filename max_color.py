def color(col):
    if col == 'black':
        return (0, 0, 0)
    if col == 'white':
        return (255, 255, 255)
    if col == 'red':
        return (255, 0, 0)
    if col == 'green':
        return (0, 255, 0)
    if col == 'blue':
        return (0, 0, 255)
    if col == 'light_blue':
        return (0, 100, 255)
